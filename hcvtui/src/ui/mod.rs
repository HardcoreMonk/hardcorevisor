//! UI 화면 모듈
//!
//! 6개 화면 각각의 렌더링 함수를 정의한다.
//! 모든 화면은 `render(frame: &mut Frame, app: &App)` 시그니처를 따르며,
//! 즉시 모드(Immediate Mode)로 매 프레임 전체를 다시 그린다.
//!
//! ## 공통 레이아웃 구조
//!
//! ```text
//! ┌─── 타이틀 바 (3줄) ──────────────────────┐
//! │  HardCoreVisor │ 화면명 │ 연결상태 │ 도움 │
//! ├─── 콘텐츠 영역 (가변) ───────────────────┤
//! │  (화면별 고유 레이아웃)                    │
//! ├─── 상태 바 (1줄) ────────────────────────┤
//! │  [1]Dash [2]VMs [3]Storage ...           │
//! └──────────────────────────────────────────┘
//! ```

/// 대시보드 화면: 클러스터 개요를 2x2 그리드로 표시
pub mod dashboard;
/// HA 모니터 화면: 클러스터 상태 + 노드 테이블
pub mod ha_monitor;
/// 로그 뷰어 화면: 이벤트 로그를 자동 스크롤로 표시
pub mod log_viewer;
/// 네트워크 뷰 화면: SDN 존, 가상 네트워크, 방화벽 규칙
pub mod network_view;
/// 스토리지 뷰 화면: 스토리지 풀 + 볼륨 목록
pub mod storage_view;
/// VM 매니저 화면: VM 테이블 + 생명주기 제어 + 생성/상세 팝업
pub mod vm_manager;
